package com.composition;

public class Cloths {
    private int shirt;
    private int pant;
    private int trousers;

    public Cloths(int shirt, int pant, int trousers) {
        this.shirt = shirt;
        this.pant = pant;
        this.trousers = trousers;
    }

    public int getShirt() {
        return shirt;
    }

    public int getPant() {
        return pant;
    }

    public int getTrousers() {
        return trousers;
    }


}
